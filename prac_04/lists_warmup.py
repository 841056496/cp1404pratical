numbers = [3, 1, 4, 1, 5, 9, 2]

# The value of numbers[0] is 3
# The value of numbers[-1] is 2
# The value of numbers[3] is 1
# The value of numbers[:-1] is [3, 1, 4, 1, 5, 9]
# The value of numbers[3:4] is [1]
# The value of 5 in numbers is True
# The value of 7 in numbers is False
# The value of "3" in numbers is False
# The value of numbers + [6, 5, 3] is [3, 1, 4, 1, 5, 9, 2, 6, 5, 3]

numbers[0] = "ten"

numbers[-1] = 1

print(numbers[2:])

print(9 in numbers)
