import random

print(random.randint(5, 20))  # line 1
#The smallest number I can see is 5 and the largest is 20.

print(random.randrange(3, 10, 2))  # line 2
#The smallest number I can see is 3 and the largest is 9.
# This could not have produced a 4 since 4 is not in the sequence.

print(random.uniform(2.5, 5.5))  # line 3
#The smallest number I can see is 2.5 and the largest is 5.5.

print(random.randint(1, 100))
