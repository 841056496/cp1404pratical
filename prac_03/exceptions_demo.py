#1.A ValueError will occur if the input for either the numerator or the denominator is not a valid integer.
#2.A ZeroDivisionError will occur if the denominator is zero.
#3.yes.
try:
    numerator = int(input("Enter the numerator: "))
    denominator = int(input("Enter the denominator: "))
    while denominator == 0:
        print("Denominator cannot be zero, please enter a non-zero number.")
        denominator = int(input("Enter the denominator: "))
    fraction = numerator / denominator
    print(fraction)
except ValueError:
    print("Numerator and denominator must be valid numbers!")
print("Finished.")
